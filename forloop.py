#####################for loop#######################
# 
# range function 
# 
# range(start,stop,step)
for i in range(5):
    print(i)



for j in range(1,10,2):
    print(j)    



# using break with if
# 
for g in range(1,11):
    if(g == 5):
        break
    print(g)
    


# using continue inside if ####
for g in range(1,11):
   
    if(g == 5):
        continue
    print(g)
for p in range(1,12):
    if((p==3) or (p == 7)):
       continue
    print(p);






            